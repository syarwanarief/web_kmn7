        <p align="center"><em>KNOWLEDGE  MANAGEMENT</em> <br />
            PT PERKEBUNAN  NUSANTARA VII<br />
            (KMN7) </p>
        <p align="justify">&nbsp;</p>


        <p align="justify">A. Pendahuluan</p>



        <p align="justify">&emsp;<em>Knowledge Management</em> (KM) merupakan suatu  rangkaian kegiatan yang digunakan  perusahaan untuk <strong> mengidentifikasikan</strong>, <strong>menciptakan</strong>, <strong>menjelaskan</strong> dan <strong>mendistribusikan</strong> pengetahuan untuk digunakan kembali, diketahui, dipelajari dalam perusahaan  tersebut.</p>
                <p align="justify"><img src="<?php echo base_url('assets/images/Untitled-1_clip_image002_0000.jpg')?>" style="float:left;"/></p>
       <br />
        <p align="justify">	KMN7  terkait dengan <strong>sasaran objektif</strong> perusahaan dan <strong>ditujukan</strong> untuk  mencapai suatu hasil tertentu seperti <strong>pengetahuan  bersama</strong>, <strong>peningkatan kinerja</strong>, <strong>keunggulan kompetitif</strong>, atau <strong>tingkat inovasi</strong> yang lebih tinggi.</p>

        <p align="justify">KMN7  meliputi <strong>pengelolaan Sumber Daya Manusia</strong> dan <strong>Teknologi Informasi</strong> yang semakin  memegang peranan penting dalam pengelolaan pengetahuan dalam rangka mendukung  untuk memastikan keberlangsungan perusahaan.</p>
        <p align="justify">Jenis  pengelolaan pengetahuan dalam KMN7 meliputi <strong><em>Tacit Knowledge</em></strong> dan <strong><em>Explicit  Knowledge</em>.</strong> Pengelolaan <em>explicit  knowledge</em> umumnya tidak menjadi masalah karena mudah didokumentasikan,  berbeda dengan <em>tacit knowledge</em>,  terasa sulit untuk didokumentasikan  karena  bagi beberapa orang pengetahuan sering kali dirasakan sangat berharga untuk  dibagikan dan digunakan dengan cara yang tepat bagi orang lain.</p>
        <div align="justify">
            <br />
            <ol>
                <img src="<?php echo base_url('assets/images/Untitled-1_clip_image004_0000.jpg')?>" style="float:right;"/>
                <li><em>Tacit  knowledge </em> </li>
            </ol>
        </div>
        <p align="justify"><em>Tacit knowledge</em> bersifat personal,  dikembangkan melalui pengalaman yang sulit untuk difomulasikan dan  dikomunikasikan. Biasanyatidak  dinyatakan dalam bentuk tulisan melainkan sesuatu yang terdapat dalam benak  orang-orang yang bekerja dalam suatu organisasi/perusahaan, secara umum  dijabarkan sebagai berikut:</p>
        <div align="justify">
            <ol type="a">
                <li>Pemahaman dan aplikasi  pikiran bawah sadar,</li>
                <li>Susah untuk diucapkan,</li>
                <li>Berkembang dari kejadian  langsung dan pengalaman</li>
                <li type="a">Berbagi pengetahuan melalui  percakapan (<em>story-telling</em>)</li>
            </ol>
        </div>
        <p align="justify"><em>Tacit knowledge</em> dapat diartikan sebagai <em>personal knowledge</em> yang didapat dari  instruksi formal dan informal yang dalamnya juga termasuk <em>story-telling</em>, hubungan pribadi, buku yang telah dibaca atau  ditulis, catatan, dokumen, foto, intuisi, pengalaman dan segala sesuatu yang  dipelajari.</p>
                <br />
        <p align="justify">2. <em>Explicit  knowledge</em> <img src="<?php echo base_url('assets/images/Untitled-1_clip_image002.jpg')?>" width="40%" style="float:right;"/></p>

        <p align="justify"><em>Explicit knowledge</em> bersifat formal dan  sistematis yang mudah untuk dikomunikasikan dan dibagi, bergantung pada  pemahaman dan aplikasi secara <em>tacit</em>,  maka dari itu semua pengetahuan berakar dari tacit <em>knowledge.</em></p>
        <p align="justify"><em>Explicit  knowledge</em> dapat dijabarkan sebagai:</p>
        <div align="justify">
            <ol type="a">
                <li>Dapat diucapkan secara tepat  dan resmi,</li>
                <li>Mudah disusun,  didokumentasikan, dipindahkan, dibagi dan dikomunikasikan.</li>
            </ol>
        </div>

        <p align="justify">Penerapan e<em>xplicit knowledge</em> lebih mudah karena  pengetahuan yang diperoleh dalam bentuk tulisan, atau pernyataan yang  didokumentasikan, sehingga setiap karyawan dapat mempelajarinya secara mandiri.</p>
        <p align="justify">Elemen pokok lainnya yang  mendukung pengelolaan KMN7 adalah <strong>TEKNOLOGI</strong>,  dimana digunakan sebagai media untuk mempermudah penyebaran pengetahui baik e<em>xplicit knowledge</em> maupun <em>tacit</em> <em>knowledge</em> yang telah dimodifikasi menjadi e<em>xplicit knowledge.</em></p>



        <p align="justify">Salah satu teknologi  digunakan untuk proses penyebaran pengetahuan dalam KMN7 adalah <strong>internet</strong>, dimana Karyawan dapat  mengakses pengetahuan dan melakukan kolaborasi serta <strong><em>sharing knowledge</em></strong> secara <strong><em>on  line</em></strong>.</p>
        <p align="justify"><img src="<?php echo base_url('assets/images/Untitled-1_clip_image004.jpg')?>" style="float:right;"/>Elemen pokok dalam  pengelolaan KMN7 adalah :</p>
        <div align="justify">
            <ol type="a">
                <li><strong><em>People</em></strong> : Personal yang terlibat  dalam pengelolaan pengetahuan, dimana KMN7 berasal dari Karyawan dan sebagai  bentuk dasar untuk membentuk pengetahuan baru.</li>
                <li><strong><em>Technology</em></strong>: merupakan  infrasrtuktur teknologi yang standar, konsisten dan dapat diandalkan dalam  mendukung alat-alat perusahaan.</li>
                <li><strong><em>Processes</em></strong>: yaitu  suatu rangkaian menangkap, menyaring, mengesahkan, mentransformasikan dan  menyebarkan pengetahuan kepada seluruh elemen perusahaan dilengkapi dengan  menjalankan prosedur dan proses tertentu.</li>
            </ol>
        </div>


        <p align="justify">Tujuan penerapan <em>Knowledge Manajemen</em> antara lain:</p>
        <div align="justify">
            <ol>
                <li><strong>Penghematan  waktu dan biaya.</strong> Dengan adanya sumber pengetahuan yang terstruktur dengan baik, maka perusahaan  akan mudah untuk menggunakan pengetahuan tersebut untuk konteks yang lainnya,  sehingga perusahaan akan dapat menghemat waktu dan biaya.</li>
                <li><strong>Peningkatan  aset pengetahuan</strong>.  Sumber pengetahuan akan memberikan kemudahan kepada setiap karyawan untuk  memanfaatkannya, sehingga proses pemanfaatan pengetahuan di lingkungan  perusahaan akan meningkat, yang akhirya proses kreatifitas dan inovasi akan  terdorong lebih luas dan setiap karyawan dapat meningkatkan kompetensinya.</li>
                <li><strong>Kemampuan  beradaptasi</strong>.  Perusahaan akan dapat dengan mudah beradaptasi dengan perubahan lingkungan  bisnis yang terjadi.</li>
                <li><strong>Peningkatan  produktivitas</strong>.  Pengetahuan yang sudah ada dapat digunakan ulang untuk proses atau produk yang  akan dikembangkan, seingga produktivitas dari perusahaan akan meningkat.</li>
            </ol>
        </div>


